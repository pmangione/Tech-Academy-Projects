using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ExercisePage36
{
    class MathOperationsOnNumber
    {
        public int subtractTwentyFromNumber(int userNumber)
        {
            return userNumber - 20;
        }

        public int multiplyNumberByTwo(int userNumber)
        {
            return userNumber * 2;
        }

        public int addOneToNumber(int userNumber)
        {
            return userNumber + 1;
        }
    }
}
